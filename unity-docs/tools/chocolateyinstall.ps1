$checksum64     = 'dfef8f246ede2709cb214e51b383013880be2fa00f60c784272c3496760d2eac'

$packageArgs = @{
  packageName    = $packageName
  fileType       = 'EXE'
  url64bit       = $url64
  checksum64     = $checksum64
  checksumType64 = 'sha256'
  silentArgs     = '/S'
  validExitCodes = @(0)
}

